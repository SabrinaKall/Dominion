
CREATE TABLE IF NOT EXISTS USER (ID INTEGER PRIMARY KEY AUTO_INCREMENT,
                                  NAME VARCHAR NOT NULL, regDate DATE DEFAULT CURRENT_DATE(), PASSWORD VARCHAR);



CREATE TABLE IF NOT EXISTS GameInfo
(gid INTEGER PRIMARY KEY AUTO_INCREMENT,
winner INTEGER NOT NULL,
gamedate DATE NOT NULL,
FOREIGN KEY (winner) REFERENCES User(ID));

CREATE TABLE IF NOT EXISTS USERGAMEINFO
(UID INTEGER NOT NULL,
GID INTEGER NOT NULL,
FOREIGN KEY (UID) REFERENCES USER (ID),
FOREIGN KEY (GID) REFERENCES GAMEINFO,
PRIMARY KEY (UID, GID));

CREATE TABLE IF NOT EXISTS GAMESTATE (ID INTEGER PRIMARY KEY AUTO_INCREMENT, STATE BLOB NOT NULL, USERS ARRAY);


